import requests
import csv
import os
from datetime import datetime
API_KEY="afffdda0d48637fcab0c94c830bbb209"
FILE_NAME="weather_log.csv"


def get_weather(city):
  url =f"https://api.openweathermap.org/data/2.5/weather?q=Pune&appid={API_KEY}&units=metric"

  try:
    response=requests.get(url)
    data =response.json()

    if data.get("cod") != 200:
      return None

    return {
        "city":city,
        "temp":data["main"]["temp"],
        "condition":data["weather"][0]["main"]

    }
  except:
    return None

def save_weather(data):
  today =datetime.now().strftime("%Y-%m-%d")
  file_exists = os.path.exists(FILE_NAME)

  if file_exists:
    with open(FILE_NAME,"r")as file:
      reader =csv.reader(file)
      for row in reader:
        if row and row[0]==today and row[1]==data["city"]:
          print("already logged today for this city")
          return
  with open(FILE_NAME,"a",newline="")as file:
    writer =csv.writer(file)

    if not file_exists:
      writer.writerow(["Date", "City", "Temperature", "Condition"])

    writer.writerow([
        today,
        data["city"],
        data["temp"],
        data["condition"]
    ])

    print("weather logged successfully")
def view_logs():
    if not os.path.exists(FILE_NAME):
        print("No data found")
        return

    with open(FILE_NAME, "r") as file:
      print(file.read())

def analytics():
    if not os.path.exists(FILE_NAME):
        print("No data found")
        return

    temps = []
    conditions = []

    with open(FILE_NAME, "r") as file:
        reader = csv.DictReader(file)

        for row in reader:
            temps.append(float(row["Temperature"]))
            conditions.append(row["Condition"])
    if len(temps) == 0:
        print("No data for analytics")
        return

    print("\n--- ANALYTICS ---")
    print("Average Temp:", sum(temps)/len(temps))
    print("Max Temp:", max(temps))
    print("Min Temp:", min(temps))
    print("Most Frequent Condition:", max(set(conditions), key=conditions.count))

def main():
    while True:
        print("\n1. Add Weather Log")
        print("2. View Logs")
        print("3. Analytics")
        print("4. Exit")

        choice = input("Enter choice: ")

        if choice == "1":
            city = input("Enter city: ")
            data = get_weather(city)

            if data:
                save_weather(data)
            else:
                print("Invalid city or API error")

        elif choice == "2":
            view_logs()

        elif choice == "3":
            analytics()

        elif choice == "4":
            break

        else:
            print("Invalid choice")

if __name__ == "__main__":
    main()

