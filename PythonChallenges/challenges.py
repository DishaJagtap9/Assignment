import json
import os
file_name="movie.json"
def load_movie():
  if not os.path.exists(file_name):
    return[]
  with open(file_name,"r")as file:
    try:
      return json.load(file)
    except json.JSONDecodeError:
      return []

def save_movie(movies):
    with open(file_name,"w") as file:
      json.dump(movies,file,indent=4)
def add_movie():
    movies =load_movie()
    title =input("enter movie title:").strip().lower()
    genre =input("enter genre:").strip()
    rating=input("enter rating:").strip()
    movie={
        "title":title,
        "genre":genre,
        "rating":rating
    }
    movies.append(movie)
    save_movie(movies)
    print("movie added successfully")
def view_movie():
  movies =load_movie()

  if not movies:
    print("no movies found")
    return
  print("\nMovie list:")
  for i, movie in enumerate(movies, start=1):
        print(f"{i}. {movie['title']} | {movie['genre']} | {movie['rating']}")
