#7. Sort a list of alphanumeric strings based on product value of numeric character in it.
# If in any string there is no numeric character take it's product value as 1.
string=['1ac21', '23fg', '456', '098d','1','kls']
def product_of_digit(s):
    product=1
    has_digit=False
    for i in s:
      if i.isdigit():
        product*=int(i)
        has_digit=True
    if not has_digit:
     product=1
    return product
sorted_string=sorted(string,key=product_of_digit)
print(sorted_string)

def numeric_value(s):
    digits = ''.join([c for c in s if c.isdigit()])
    if digits == '':
        return 1
    else:
        return int(digits)

string_list = ['1ac21', '23fg', '456', '098d', '1', 'kls']
sorted_list = sorted(string_list, key=numeric_value, reverse=True)

print(sorted_list)

