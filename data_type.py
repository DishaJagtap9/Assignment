#18. Write a python function that receives a list of integers and prints out a histogram of bin size 10
list1=[13,42,15,37,22,39,41,50]
def histogram(list1):
  bins={}
  for num in list1:
    bin_range=(num//10)*10
    if bin_range not in bins:
      bins[bin_range]=0
    bins[bin_range]+=1
  for b in sorted(bins):
      print(f"{b}--{b+9}:{'*' * bins[b]}")
